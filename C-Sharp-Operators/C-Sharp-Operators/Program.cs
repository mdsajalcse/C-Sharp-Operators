//C# Operators

//#region Example 1 - Increment/Decrement operator

Console.WriteLine("Double Pre & Post increment Data Type:");
int i = 3;
Console.WriteLine(i);
Console.WriteLine(++i);
Console.WriteLine(i++);
Console.WriteLine(i);
Console.WriteLine(++i);
Console.WriteLine(i++);
Console.WriteLine(i);

//Double data type
Console.WriteLine("Double Pre Data Type:");
double d = 1.4;
Console.WriteLine(d);
Console.WriteLine(++d);
Console.WriteLine(d);

//int data type
Console.WriteLine("Int pre Decrement Data Type");
int i2 = 12;
Console.WriteLine(i2);
Console.WriteLine(--i2);
Console.WriteLine(i2);

Console.WriteLine("Int post Decrement Data Type");
int i3 = 3;
Console.WriteLine(i3);
Console.WriteLine(i3--);
Console.WriteLine(i3);